    public String voksenFunktion()
    {
        Object o = spinnerVoksen.getValue();
        Number n = (Number) o;

        int voksenValue = n.intValue();
        int voksenP = billetTyper.getVoksenpris();

        int voksenTotal = voksenValue * voksenP;
        String voksenPris = Integer.toString(voksenTotal);
        return voksenPris;
    }

    public String borneFunktion()
    {
        Object o = spinnerBarn.getValue();
        Number n = (Number) o;

        int barnValue = n.intValue();
        int barnP = billetTyper.getBornepris();

        int barnTotal = barnValue * barnP;
        String bornePris = Integer.toString(barnTotal);
        return bornePris;
    }

    public int textTotalFunktion()
    {
        Object objVoksen = spinnerVoksen.getValue();
        Number numVoksen = (Number) objVoksen;

        int voksenValue = numVoksen.intValue();
        int voksenP = billetTyper.getVoksenpris();
        int voksenTotal = voksenValue * voksenP;


        Object objBarn = spinnerBarn.getValue();
        Number numBarn = (Number) objBarn;

        int barnValue = numBarn.intValue();
        int barnP = billetTyper.getBornepris();
        int barnTotal = barnValue * barnP;

        int total = voksenTotal + barnTotal;
        return total;

    }

