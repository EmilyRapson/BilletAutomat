    public String totalBalance()
    {

        int balanceBetaling = betaling.getBalance();

        String totalbalance = Integer.toString(balanceBetaling);

        if (balanceBetaling < textTotalFunktion() )
        {
            balanceTextfelt.setText(totalbalance);
        }
        else
        {
            GUIBilletKvitering b = new GUIBilletKvitering();
            b.setVisible(true);
        }

        return totalbalance;
    }

